#pragma once

#define KEYCODE_H

#define LEFTKEY		0xe04b	//화살표(왼쪽) 키
#define RIGHTKEY    0xe04d  //화살표(오른쪽) 키
#define UPKEY	    0xe048	//화살표(위쪽) 키
#define DOWNKEY		0xe050	//화살표(아래쪽) 키
#define ESCKEY		0x1b	//ESC (종료)
#define PREVKEY		0x71	//Q (이전스테이지)
#define SAVEKEY		0x73    //S (맵 save)
#define LOADKEY		0x6c	//L (맵 load)
#define NEXTKEY		0x77	//W (다음스테이지)
#define RESETKEY    0x72	//R (리셋)
#define ROLLBACKKEY   0x70	//P 
#define ENTERKEY    0x0d	//ENTER (입력)
#define BACKKEY		0x08	//BACK (입력)

